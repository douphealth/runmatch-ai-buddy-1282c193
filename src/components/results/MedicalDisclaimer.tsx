import { AlertTriangle } from 'lucide-react';

interface MedicalDisclaimerProps {
  variant?: 'banner' | 'inline' | 'compact';
}

/**
 * Educational-only / not-medical-advice disclaimer.
 *
 * Required because the app makes recommendations tied to injury history
 * (shin splints, IT band, plantar fasciitis, etc.). Without this notice
 * we expose ourselves to liability if a user follows a recommendation
 * and worsens an existing injury.
 *
 * Variants:
 *  - banner: full call-out, used inside the injury-prevention section
 *  - inline: compact card, used at the foot of the page
 *  - compact: small inline note for tight spaces
 */
const MedicalDisclaimer = ({ variant = 'banner' }: MedicalDisclaimerProps) => {
  if (variant === 'compact') {
    return (
      <p className="text-[11px] text-muted-foreground leading-relaxed">
        <span className="font-semibold text-foreground">Educational only.</span>{' '}
        Not medical advice. Consult a qualified clinician before changing
        footwear if you have a current injury.
      </p>
    );
  }

  if (variant === 'inline') {
    return (
      <div className="rounded-xl border border-warning/30 bg-warning/5 px-4 py-3 flex items-start gap-3">
        <AlertTriangle className="w-4 h-4 text-warning flex-shrink-0 mt-0.5" />
        <p className="text-xs text-muted-foreground leading-relaxed">
          <span className="font-semibold text-foreground">Educational content — not medical advice.</span>{' '}
          RunMatch AI is an informational tool. It is not a substitute for diagnosis or
          treatment from a sports physician, podiatrist, or physical therapist.
          If you are recovering from or living with an injury, consult a qualified
          clinician before changing your footwear or training plan.
        </p>
      </div>
    );
  }

  return (
    <div className="rounded-2xl border-2 border-warning/30 bg-warning/5 p-5 flex items-start gap-4">
      <div className="w-10 h-10 rounded-xl bg-warning/15 flex items-center justify-center flex-shrink-0">
        <AlertTriangle className="w-5 h-5 text-warning" />
      </div>
      <div className="flex-1">
        <h3 className="text-sm font-bold uppercase tracking-wider text-foreground mb-2">
          Educational content — not medical advice
        </h3>
        <p className="text-xs md:text-sm text-muted-foreground leading-relaxed">
          The recommendations on this page are generated by a deterministic
          scoring algorithm and are intended for general educational purposes.
          They do not constitute medical advice, diagnosis, or treatment.
          If you are currently injured, recovering from an injury, or experience
          pain while running, consult a qualified sports physician, podiatrist,
          or physical therapist before acting on any recommendation here.
        </p>
      </div>
    </div>
  );
};

export default MedicalDisclaimer;
